package com.learning.api.security;

import com.learning.api.entity.User;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Collections;

/**
 * ============================================================
 * INTERVIEW CONCEPT: UserDetails Interface
 * ============================================================
 *
 * Implements Spring Security's UserDetails. Represents the authenticated principal.
 *
 * GrantedAuthority:
 * - Maps database roles to Spring Security authorities (e.g. ROLE_USER).
 * - Spring security expects roles to be prefixed with "ROLE_" to use methods
 *   like .hasRole("USER") or @PreAuthorize("hasRole('USER')").
 */
public class UserDetailsImpl implements UserDetails {

    private final User user;

    public UserDetailsImpl(User user) {
        this.user = user;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return Collections.singletonList(new SimpleGrantedAuthority(user.getRole()));
    }

    @Override
    public String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getEmail(); // We use email as our username/credential
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }

    public User getUser() {
        return this.user;
    }
}
